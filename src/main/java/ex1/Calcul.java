public class Calcul {

	/**
	 * 
	 * @param a
	 * @param b
	 * @return Addition de a et b
	 */
	public int doAdd(int a, int b){
		return a+b;
	}
	
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return Soustraction  de  a et  b
	 */
	public int doSub(int a, int b){
		return a-b;
	}
}
